package library_mgmt_system;

public class Borrower {
	String FName;
	String Address;
	String Phone;
	String LName;
	int CardNo;

	public Borrower(String fName, String address, String phone, String lName,
			int cardNo) {
		FName = fName;
		Address = address;
		Phone = phone;
		LName = lName;
		CardNo = cardNo;
	}

}
